# Write a program to iterate the first 10 numbers, and in each iteration, print the sum of the current and previous number.

# Pseudocode

# Provide the first number
current_number = 0
# Iterate the first 10 numbers
for i in range(10):
# Solve  for the sum of the current and previous numbers
    current_sum = current_number + i
# Print the sum
    print(current_number, current_sum, i)

# Update the current number
    current_number = i

